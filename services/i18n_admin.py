def ta(lang: str, key: str, **kwargs) -> str:
    """Translate an admin-panel key for lang, falling back to English."""
    table = ADMIN.get(lang) or ADMIN["en"]
    text = table.get(key) or ADMIN["en"].get(key) or key
    if kwargs:
        try:
            text = text.format(**kwargs)
        except (KeyError, IndexError):
            pass
    return text
